var marks = prompt("enter the number ");
if (marks > 80)
    document.write("A+");
else if (marks > 70)
    document.write("A");
else if (marks > 65)
    document.write("A-");
else if (marks > 60)
    document.write("B");
else if (marks > 40)
    document.write("C");
else if (marks > 33)
    document.write("D");
else
    document.write("Fail");